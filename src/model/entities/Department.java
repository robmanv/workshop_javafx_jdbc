package model.entities;

import java.io.Serializable;

public class Department implements Serializable { // O serializable permite que o objeto seja convertido em uma sequencia de caracteres, para trafego via arquivo etc
    /**
     *
     */
    private static final long serialVersionUID = 1L;
    
    private Integer codigo;
    private String nome;

    public Department(){

    }

    public Department(Integer codigo, String nome) {
        this.codigo = codigo;
        this.nome = nome;
    }

    public Integer getCodigo() {
        return codigo;
    }

    public void setCodigo(Integer codigo) {
        this.codigo = codigo;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((codigo == null) ? 0 : codigo.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        Department other = (Department) obj;
        if (codigo == null) {
            if (other.codigo != null)
                return false;
        } else if (!codigo.equals(other.codigo))
            return false;
        return true;
    }

    @Override
    public String toString() {
        return "Department [codigo=\" + codigo + \", nome=" + nome + "]";
    }
    
}